package com.easygarden.model;

public class Person {

	private int perid;
	private String pernom;
	private String perapp;
	private String perapm;
	private String pertel;
	private String percel;
	private String status;
	public int getPerid() {
		return perid;
	}
	public void setPerid(int i) {
		this.perid = i;
	}
	public String getPernom() {
		return pernom;
	}
	public void setPernom(String pernom) {
		this.pernom = pernom;
	}
	public String getPerapp() {
		return perapp;
	}
	public void setPerapp(String perapp) {
		this.perapp = perapp;
	}
	public String getPerapm() {
		return perapm;
	}
	public void setPerapm(String perapm) {
		this.perapm = perapm;
	}
	public String getPertel() {
		return pertel;
	}
	public void setPertel(String pertel) {
		this.pertel = pertel;
	}
	public String getPercel() {
		return percel;
	}
	public void setPercel(String percel) {
		this.percel = percel;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
}
